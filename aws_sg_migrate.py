def main():
    try:
        opts, args = getopt.getopt(sys.argv[1:], "hp:sv:", [ "help", "profile=", "shell", "vpc=", "src=", "dest=", ])
    except getopt.GetoptError:
        usage()
        sys.exit(2)
    
    profile = None
    vpcid = None
    shell = False
    source = None
    destination = None
    for o,a in opts:
        if o in ("-h", "--help"):
            usage()
            return
        if o in ("-p", "--profile"):
            profile = a
        if o in ("-s", "--shell"):
            shell = True
        if o in ("-sc", "--src"):
            source = a
        if o in ("-ds", "--dest"):
            destination = a
        if o in ("-v", "--vpc"):
            vpcid = a

    if len(args) != 1:
        print("ERROR: You must give a security group id")
        usage()
        sys.exit(1)
    sgid = args[0]

    makesg(profile, sgid, vpcid, source, destination, shell)

